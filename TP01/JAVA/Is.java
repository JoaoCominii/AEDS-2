    // Verificar se a palavra Ã© composta apenas por vogais
    public static boolean verificarVogais(String palavra) {
        for (int i = 0; i < palavra.length(); i++) {
            if (palavra.charAt(i) != 'a' && palavra.charAt(i) != 'e' && palavra.charAt(i) != 'i' && palavra.charAt(i) != 'o' && palavra.charAt(i) != 'u') {
                return false;
            }
        }
        return true;
    }

    // Verificar se a palavra Ã© composta apenas por consoantes
    public static boolean verificarConsoantes(String palavra) {
        for (int i = 0; i < palavra.length(); i++) {
            char c = palavra.charAt(i);
            if (!(c >= 'a' && c <= 'z' && c != 'a' && c != 'e' && c != 'i' && c != 'o' && c != 'u')) {
                return false;
            }
        }
        return true;
    }

    // Verificar se a palavra corresponde a um nÃºmero inteiro
    public static boolean verificarInteiro(String palavra) {
        for (int i = 0; i < palavra.length(); i++) {
            if (!Character.isDigit(palavra.charAt(i))) {
                return false;
            }
        }
        return true;
    }

    // Verificar se a palavra corresponde a um nÃºmero real
    public static boolean verificarReal(String palavra) {
        int pontos = 0;
        for (int i = 0; i < palavra.length(); i++) {
            char c = palavra.charAt(i);
            if (c == '.' || c == ',') {
                pontos++;
            } else if (!Character.isDigit(c)) {
                return false;
            }
        }
        return pontos <= 1;
    }
